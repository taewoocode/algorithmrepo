package chap2;

public class Car {
    private int door;
    private int wheel;

    public Car() {
        this.door = door + 4;
        this.wheel = wheel + 4;
    }

    public int getDoor() {
        return door;
    }

    public int getWheel() {
        return wheel;
    }

    public void setDoor(int door) {
        this.door = door;
    }

    public void setWheel(int wheel) {
        this.wheel = wheel;
    }
}
